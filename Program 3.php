#Write a program to create a menu driven program and to show the usage of switch case.

<?php
$meal="Breakfast";
switch($meal){
    case"Breakfast":
        echo"It's Breakfast time";
        break;
    case"Lunch":
        echo"It's Lunch time";
        break;
    case"Dinner":
        echo"It's Dinner time";
        break;
    default:
        echo"It's snacks time";
    }
 
?>






